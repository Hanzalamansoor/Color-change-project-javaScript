#type casting 

a = 4
print(type(a))

b = 4.25
print(type(b))

c = "Hello"
print(type(c))

d = True
print(type(d))

#now we can use type function to change the variable for example :
t = "5.25" # this is a string
e = float(t) # thats how you change string into a float
f = type(e)
print(f) # your final answer will be the float 
