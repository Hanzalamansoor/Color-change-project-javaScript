# IN WHILE LOOP FIRST THE CONDITION IS CHECKED , IF THE CONDITION IS TRUE THEN OPERATION WILL CONTINUE AND IF THE CONDITION IS FALSE OPERATION WILL NOT CONTINUE IT WILL END THE PROGRAM RIGHT THERE

i = 1
while(i<6) :  #this condition is true so it will execute the program 
     print(i)
     i+=1 # This line of code will increment it by 1 and then check it again in the while loop if the condition is true or not 

# i value is 1 so it will keep incrementing by 1 until it will reach 6 < 6 which is not true so it will exit the program
